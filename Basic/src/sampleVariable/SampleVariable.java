package sampleVariable;

public class SampleVariable {
public static void main(String[] args) {
	//変数のサンプル
	String str = "Schoo"; //文字列
	char ch = 's'; //文字
	int i = 100; //整数
	float f = 3.14F;//実数

	//変数の出力
	System.out.println(str);
	System.out.println(ch);
	System.out.println(i);
	System.out.println(f);

}
}
